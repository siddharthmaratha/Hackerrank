#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    int i;
    long k;
    float b;
    char ch;
    double d;
    scanf("%d %ld %c %f %lf", &i, &k, &ch, &b, &d);
    printf("%d\n", i);
    printf("%ld\n", k);
    printf("%c\n", ch);
    printf("%f\n",b);
    printf("%lf\n",d);
    return 0;
}
